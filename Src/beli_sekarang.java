    private void btnBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBeliActionPerformed
        if(txtAlamat.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi alamat", "Data belum lengkap", JOptionPane.INFORMATION_MESSAGE);
            txtAlamat.requestFocus();
            return;
        }
        else if(txtJumlah.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Silahkan isi jumlah barang", "Data belum lengkap", JOptionPane.INFORMATION_MESSAGE);
            txtJumlah.requestFocus();
            return;
        }
        try {
               String sql = "Select * from dataobat where id_obat= ?";
               Connection conn = koneksi.koneksiDB();
               PreparedStatement statement = conn.prepareStatement(sql);
               statement.setInt(1, menu_pesanan_user.idObat);
               ResultSet rs = statement.executeQuery();
               while(rs.next()){
                   if(menu_pesanan_user.idObat == menu_pesanan_user.idObat){
                       String username = Logininterface.usernamee;
                       int idobat = rs.getInt("id_obat");
                       String nama_obat = rs.getString("nama_obat");
                       String jenis_obat = rs.getString("jenis_obat");
                       //String ketersediaan = rs.getString("ketersediaan");
                       String dosis = rs.getString("dosis");
                       Date expired_obat = rs.getDate("expired_obat");
                       int hargaobatt = rs.getInt("harga_obat");

                       
                       String insertQuery = "INSERT INTO pesanan_user (id_obat, username, nama_obat, jenis_obat, dosis, expired_obat, jumlah_barang, harga_obat, alamat_pesanan) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
                       PreparedStatement inserstatment = conn.prepareStatement(insertQuery);
                       inserstatment.setInt(1, idobat);
                       inserstatment.setString(2, username);
                       inserstatment.setString(3, nama_obat);
                       inserstatment.setString(4, jenis_obat);
                       inserstatment.setString(5, dosis);                               
                       inserstatment.setDate(6, new java.sql.Date(expired_obat.getTime()));
                       inserstatment.setString(7, txtJumlah.getText());                                       
                       inserstatment.setInt(8, simpan_harga);                                       
                       inserstatment.setString(9, txtAlamat.getText()); 
                       
                       String updatestockbarang = "Update dataobat Set stock_obat=? where id_obat= ?";                       
                       PreparedStatement updatestatmen = conn.prepareStatement(updatestockbarang);                       
                       updatestatmen.setInt(1, stock_barang);
                       updatestatmen.setInt(2, menu_pesanan_user.idObat);
                       updatestatmen.executeUpdate();
                       if (txtJumlah.getText().isEmpty()){
                           JOptionPane.showMessageDialog(null, "Harap isi jumlah barangt.", "Peringatan", JOptionPane.WARNING_MESSAGE);
                           return;
                       }
                       int confirm = JOptionPane.showConfirmDialog(null, "Anda yakin ingin melakukan pembelian?", "Konfirmasi Pembelian", JOptionPane.YES_NO_OPTION);
                       if (confirm == JOptionPane.YES_OPTION) {
                           inserstatment.executeUpdate();
                           JOptionPane.showMessageDialog(null, "Berhasil Membeli Obat", "Success", JOptionPane.INFORMATION_MESSAGE);
                       } 
                       break;
                   }                

               }
           } catch (SQLException ex) {               
               Logger.getLogger(menu_pesanan_user.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnBeliActionPerformed

