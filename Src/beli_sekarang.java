    public void ambil_data(){
        try {
            System.out.println("ini id obat beli sekarang" + menu_pesanan_user.idObat);
            String sql = "Select nama_obat,harga_obat, stock_obat, jenis_obat, ketersediaan, dosis, expired_obat, deskripsi_obat From dataobat where id_obat= ?";
            Connection conn = koneksi.koneksiDB();
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setInt(1, menu_pesanan_user.idObat);
            ResultSet rs = statement.executeQuery();            
            while(rs.next()){                                                
                if(menu_pesanan_user.idObat == menu_pesanan_user.idObat){
                    String nama_obat = rs.getString("nama_obat");
                    String harga_obat = rs.getString("harga_obat");
                    String stock_obat = rs.getString("stock_obat");
                    String jenis_obat = rs.getString("jenis_obat");                                
                    String dosis = rs.getString("dosis");
                    Date expired_obat = rs.getDate("expired_obat");
                    String deskripsi_obatt = rs.getString("deskripsi_obat");                    
                    namaobat.setText(nama_obat);                    
                    NumberFormat formatRupiah = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));
                    String totalHargaRupiah = formatRupiah.format(Double.parseDouble(harga_obat));
                    harga_obatt.setText(totalHargaRupiah);

                    //namaobat.setText(nama_obat);
                    jenisobat.setText(jenis_obat);
                    dosisobat.setText(dosis);
                    expiredobat.setText(expired_obat.toString());
                    deskripsi_obat.setText(deskripsi_obatt); 
                    break;
                }
                //System.out.println("datanya" + menu_pesanan_user.idObat);
            }            
        } catch (SQLException ex) {
            Logger.getLogger(menu_pesanan_user.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

