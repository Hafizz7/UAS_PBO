    private void txtJumlahFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtJumlahFocusLost
        String total_barangg = txtJumlah.getText();        
        jumlah_barang = Integer.parseInt(total_barangg);                
        String stockbarangstring = String.valueOf(menu_pesanan_user.stock_barang);
        if(jumlah_barang > menu_pesanan_user.stock_barang){
            txtJumlah.setText(stockbarangstring);
            JOptionPane.showMessageDialog(null, "Anda Melebihi Stock Barang");
        }
        else{
            int total_hargay = jumlah_barang * total_harga;        
            NumberFormat formatRupiah = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));
            String totalHargaRupiah = formatRupiah.format(total_hargay);
            harga_obatt.setText(String.valueOf(totalHargaRupiah));
            simpan_harga = total_hargay;
            stock_barang = menu_pesanan_user.stock_barang - jumlah_barang;

        }                        
    }//GEN-LAST:event_txtJumlahFocusLost

